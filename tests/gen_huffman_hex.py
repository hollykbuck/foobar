def build_canonical_codes(bits, values):
    codebook = {}
    code = 0
    index = 0
    for bit_len, count in enumerate(bits, start=1):
        for _ in range(count):
            symbol = values[index]
            codebook[symbol] = (code, bit_len)
            code += 1
            index += 1
        code <<= 1
    if index != len(values):
        raise ValueError("Huffman bits/values length mismatch")
    return codebook


def build_huffman_mem():
    mem = [0] * 1024

    for category, (code, bit_len) in build_canonical_codes(BITS_DC_LUMA, VALS_DC_LUMA).items():
        mem[0x000 | category] = (code << 8) | bit_len
    for run_size, (code, bit_len) in build_canonical_codes(BITS_AC_LUMA, VALS_AC_LUMA).items():
        mem[0x100 | run_size] = (code << 8) | bit_len
    for category, (code, bit_len) in build_canonical_codes(BITS_DC_CHROMA, VALS_DC_CHROMA).items():
        mem[0x200 | category] = (code << 8) | bit_len
    for run_size, (code, bit_len) in build_canonical_codes(BITS_AC_CHROMA, VALS_AC_CHROMA).items():
        mem[0x300 | run_size] = (code << 8) | bit_len

    return mem


def build_header_bytes():
    data = []

    def zigzag_table(table_bytes):
        return [table_bytes[index] for index in ZIGZAG_ORDER]

    def put(*values):
        data.extend(values)

    def put_dqt(table_id, table_bytes):
        put(0xFF, 0xDB, 0x00, 0x43, table_id)
        put(*table_bytes)

    def put_dht(table_class, table_id, bits, values):
        length = 2 + 1 + 16 + len(values)
        put(0xFF, 0xC4, (length >> 8) & 0xFF, length & 0xFF, (table_class << 4) | table_id)
        put(*bits)
        put(*values)

    put(0xFF, 0xD8)
    put(0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01, 0x01, 0x00, 0x60, 0x00, 0x60, 0x00, 0x00)
    put_dqt(0x00, zigzag_table(LUMA_QTABLE))
    put_dqt(0x01, zigzag_table(CHROMA_QTABLE))
    put(0xFF, 0xC0, 0x00, 0x11, 0x08, 0x00, 0x10, 0x00, 0x10, 0x03, 0x01, 0x11, 0x00, 0x02, 0x11, 0x01, 0x03, 0x11, 0x01)
    put_dht(0, 0, BITS_DC_LUMA, VALS_DC_LUMA)
    put_dht(1, 0, BITS_AC_LUMA, VALS_AC_LUMA)
    put_dht(0, 1, BITS_DC_CHROMA, VALS_DC_CHROMA)
    put_dht(1, 1, BITS_AC_CHROMA, VALS_AC_CHROMA)
    put(0xFF, 0xDA, 0x00, 0x0C, 0x03, 0x01, 0x00, 0x02, 0x11, 0x03, 0x11, 0x00, 0x3F, 0x00)

    return data


def write_hex_file(path, values, width):
    with path.open("w", encoding="ascii") as handle:
        for value in values:
            handle.write(f"{value:0{width}x}\n")


def main():
    root = Path(__file__).resolve().parent.parent
    huffman_mem = build_huffman_mem()
    header_bytes = build_header_bytes()

    write_hex_file(root / "huffman_tables.hex", huffman_mem, 6)
    write_hex_file(root / "jpeg_header.hex", header_bytes, 2)

    print(f"Generated huffman_tables.hex with {len(huffman_mem)} entries.")
    print(f"Generated jpeg_header.hex with {len(header_bytes)} bytes.")


if __name__ == "__main__":
    main()
